package pack1;

public class A {
	
	/*
	 * void msg() 
	 * { 
	 * System.out.println("hello"); 
	 * }
	 */
	
	protected void msg()
	{
		System.out.println("hello");	
	}
}

/*
 Access Modifiers:
 	1 private
 		
 		is access only withine the class 
 		not out class not package 
 		protected can access outside of pack with inheritance concept
 	2 protected
 	3 default
 	    is access only inside  the package
 	    a variable and method decleared without any access modifier is know as default modifier
 	     
 	4 public 
 
*/